char	*ft_strlowcase(char *str)
{
	char	*str_r;

	str_r = str;
	while (*str != '\0')
	{
		if (*str > 64 && *str < 91)
		{
			*str = *str + 32;
		}
		str++;
	}
	return (str_r);
}
