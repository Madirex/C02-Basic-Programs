int	ft_str_is_alpha(char *str)
{
	int	n;
	int	c;
	int	only_alph;

	n = 0;
	only_alph = 1;
	while (str[n] != '\0')
	{
		c = str[n];
		if (!((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z')))
		{
			only_alph = 0;
		}
		n++;
	}
	if (n == 0 || only_alph == 1)
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
