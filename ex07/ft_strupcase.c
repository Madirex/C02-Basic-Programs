char	*ft_strupcase(char *str)
{
	char	*str_r;

	str_r = str;
	while (*str != '\0')
	{
		if (*str >= 97 && *str <= 122)
		{
			*str = *str - 32;
		}
		str++;
	}
	return (str_r);
}
