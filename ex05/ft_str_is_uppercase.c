int	ft_str_is_uppercase(char *str)
{
	int	n;
	int	c;
	int	check;

	n = 0;
	check = 0;
	while (str[n] != '\0')
	{
		c = str[n];
		if (!(c >= 'A' && c <= 'Z'))
		{
			check = 1;
		}
		n++;
	}
	if (n == 0 || check == 0)
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
